async function createWeb3(address = "http://127.0.0.1:5006") {
  const web3 = new Web3(new Web3.providers.HttpProvider(address));
  return web3.eth.net
    .isListening()
    .then(data => web3)
    .catch(error => Promise.reject("web3 connection error Log : " + error));
}
async function getContractPastEvents({ topics, eth }) {
  const contract = await new eth.Contract(ERC20_abi, xrunContractAddress);
  return await contract.getPastEvents("Transfer", {
    fromBlock: 0,
    toBlock: "latest",
    topics,
  });
}
async function getXRUNTokenBalanceOf({ address, eth }) {
  const contract = await new eth.Contract(ERC20_abi, xrunContractAddress);
  return await contract.methods.balanceOf(address).call();
}

